package com.company;

public class Candies {
    public String name;
    public Integer cost;

    public Candies() {
        this.name = "Candie";
        this.cost = 15;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getCost() {
        return cost;
    }

    public void setCost(Integer cost) {
        this.cost = cost;
    }
}
